    <h4>
      Requirements for Entity Classes
    </h4>
  </body>
</html></richcontent>
<node CREATED="1372752389419" ID="ID_775394457" MODIFIED="1372752389419">
<richcontent TYPE="NODE"><html>
  <head>
    
  </head>
  <body>
    <ul>
      <li>
        <p>
          The class must be annotated with the <tt>javax.persistence.Entity</tt> annotation.
        </p>
      </li>
      <li>
        <p>
          The class must have a public or protected, no-argument constructor. The class may have other constructors.
        </p>
      </li>
      <li>
        <p>
          The class must not be declared <tt>final</tt>. No methods or persistent instance variables must be declared <tt>final</tt>.
        </p>
      </li>
      <li>
        <p>
          If an entity instance be passed by value as a detached object, such as through a session bean&#8217;s remote business interface, the class must implement the <tt>Serializable</tt> interface.
        </p>
      </li>
      <li>
        <p>
          Entities may extend both entity and non-entity classes, and non-entity classes may extend entity classes.
        </p>
      </li>
      <li>
        <p>
          Persistent instance variables must be declared private, protected, or package-private, and can only be accessed directly by the entity class&#8217;s methods. Clients must access the entity&#8217;s state through accessor or business methods.
        </p>
      </li>
    </ul>
  </body>
</html></richcontent>
</node>
